#!/usr/bin/env bash
# ============================================
# スクリプト概要:
#   fbi を使って静止画を表示する薄いラッパ。
#   stop 指定時は既存画像表示のみを止める。
#
# 現在の役割:
#   - 待機画像の表示
#   - program_end 画像などの表示補助
#   - 既存 fbi 表示停止
#
# 今回の変更点:
#   - stop モードを追加
#   - 画像ファイル存在確認を追加
#   - fbi 存在確認を追加
#   - tty番号を環境変数で切替可能にした
# ============================================

# ============================================
# セクション: 基本設定
# ============================================
set -u

ARG1="${1:-}"
TTY_NUM="${FBI_TTY:-1}"

# ============================================
# セクション: 共通関数
# ============================================
stop_fbi() {
  pkill -x fbi >/dev/null 2>&1 || true
}

# ============================================
# セクション: 停止モード
# ============================================
if [ "${ARG1}" = "stop" ]; then
  stop_fbi
  exit 0
fi

# ============================================
# セクション: 入力検証
# ============================================
IMAGE_PATH="${ARG1}"

if [ -z "${IMAGE_PATH}" ]; then
  echo "usage: splash_control.sh /path/to/image.png"
  exit 1
fi

if [ ! -f "${IMAGE_PATH}" ]; then
  echo "image not found: ${IMAGE_PATH}" >&2
  exit 1
fi

if ! command -v fbi >/dev/null 2>&1; then
  echo "fbi command not found" >&2
  exit 1
fi

# ============================================
# セクション: 画像切替
# ============================================
stop_fbi
nohup fbi -T "${TTY_NUM}" -noverbose -a "${IMAGE_PATH}" >/dev/null 2>&1 &
exit 0
