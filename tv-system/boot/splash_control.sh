#!/usr/bin/env bash
# ============================================
# スクリプト概要:
#   fbi を使って静止画を表示する薄いラッパ。
#
# 現在の役割:
#   - 起動中画像の表示
#   - program_end 画像などの表示補助
#
# 今回の変更点:
#   - MVP用の最小 fbi 呼び出しを追加
#   - 既存 fbi プロセス停止処理を追加
# ============================================

# ============================================
# セクション: 基本設定
# ============================================
set -eu

IMAGE_PATH="${1:-}"

if [ -z "${IMAGE_PATH}" ]; then
  echo "usage: splash_control.sh /path/to/image.png"
  exit 1
fi

# ============================================
# セクション: 既存表示停止
# ============================================
pkill -f "fbi" || true

# ============================================
# セクション: 新規表示
# ============================================
# 必要に応じて tty 番号は環境に合わせて調整
fbi -T 1 -noverbose -a "${IMAGE_PATH}" >/dev/null 2>&1 &
